#include <stdio.h>

int main()
{

    // & | ^ ~ << >>

    // 7 => 00000 0000 0000 0000 0000 0000 0000 011
    // 9 => 0000 0000 0000 0000 0000 0000 0000 1001

    printf("7 & 9 = %d \n", 7 & 9);
    printf("7 | 9 = %d \n", 7 | 9);
    printf("7 ^ 9 = %d \n", 7 ^ 9);
    printf("~7 = %d \n", ~7);
    printf("7 << 1 = %d \n", 7 << 1);
    printf("7 >> 1 = %d \n", 7 >> 1);

    return 0;
}